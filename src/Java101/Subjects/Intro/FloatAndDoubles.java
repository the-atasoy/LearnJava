package Java101.Subjects.Intro;

public class FloatAndDoubles
{
    public static void main(String[] args)
    {
        float f1 = 10.47f; //if you do not type f end of the 10.47 this code doesn't work.
        double d1 = 7.4654846598;
        double d2 = 45.74846f; // you can type here f because a double can storage a float.


        System.out.println(f1);
        System.out.println(d1);
        System.out.println(d2);

        float i1 = 5; // you can store an integer in a float or double.
        System.out.println(i1);
        System.out.println(f1 * 7);
    }
}
